// Arrays :- It is a linear collection of same type of elements are stored together in contiguous memory spaces.
#include <iostream>
using namespace std;
int main()
{
    int marks[50];
    int marks2[50] = {1, 2, 3, 4, 5, 6, 7};
    int marks3[] = {1, 2, 3};
    cout << marks[7] << endl;
    cout << marks2[6] << endl;
    cout << marks3[2] << endl;
    return 0;
}
